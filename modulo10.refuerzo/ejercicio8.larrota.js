//Función Básica
// Definir la función
function saludar() {
console.log("¡Hola, mundo!");
}
// Usar la función
saludar(); // ¡Hola, mundo!

//Función con Parámetros
// Función que recibe datos
function saludarPersona(nombre) {
console.log("¡Hola, " + nombre + "!");
}
// Usar con diferentes nombres
saludarPersona("kevin"); 
saludarPersona("juan");