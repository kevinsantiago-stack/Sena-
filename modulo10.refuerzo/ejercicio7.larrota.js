//Bucle for (Para un número específico de veces)
// Contar del 1 al 5
for (let i = 1; i <= 5; i++) {
console.log("Número: " + i);
}

//Bucle while (Mientras una condición sea verdadera)
let contador = 0;
while (contador < 3) {
console.log("Vuelta número: " + contador);
contador++; // ¡IMPORTANTE! Sin esto es infinito
}

