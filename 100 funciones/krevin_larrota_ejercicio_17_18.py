def filtrar_mayores_kevin_santiago_larrota_cuervo(lista, n):
    return [x for x in lista if x > n]

filtrados_kevin_santiago_larrota_cuervo = filtrar_mayores_kevin_santiago_larrota_cuervo([1,5,8,2],4)
print(filtrados_kevin_santiago_larrota_cuervo)
