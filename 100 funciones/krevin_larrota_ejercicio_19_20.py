def lista_a_diccionario_kevin_santiago_larrota_cuervo(lista):
    return {i: lista[i] for i in range(len(lista))}

diccionario_kevin_santiago_larrota_cuervo = lista_a_diccionario_kevin_santiago_larrota_cuervo(["Kevin", "Santiago"])
print(diccionario_kevin_santiago_larrota_cuervo)

