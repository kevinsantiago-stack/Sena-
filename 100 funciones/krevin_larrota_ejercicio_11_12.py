def lista_pares_kevin_santiago_larrota_cuervo(n):
    return [i for i in range(n+1) if i % 2 == 0]

pares_kevin_santiago_larrota_cuervo = lista_pares_kevin_santiago_larrota_cuervo(10)
print(pares_kevin_santiago_larrota_cuervo)


