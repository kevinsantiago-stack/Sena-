def impares_hasta_n_kevin_santiago_larrota_cuervo(n):
    return [x for x in range(n+1) if x % 2 != 0]

impares_hasta_kevin_santiago_larrota_cuervo = impares_hasta_n_kevin_santiago_larrota_cuervo(10)
print(impares_hasta_kevin_santiago_larrota_cuervo)


