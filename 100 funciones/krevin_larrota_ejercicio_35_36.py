def elementos_pares_kevin_santiago_larrota_cuervo(lista):
    return [x for x in lista if x % 2 == 0]

pares_kevin_santiago_larrota_cuervo = elementos_pares_kevin_santiago_larrota_cuervo([1,2,3,4,5,6])
print(pares_kevin_santiago_larrota_cuervo)

