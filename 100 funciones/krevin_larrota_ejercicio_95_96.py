def redondear_lista_kevin_santiago_larrota_cuervo(lista):
    return [round(x) for x in lista]

redondeados_kevin_santiago_larrota_cuervo = redondear_lista_kevin_santiago_larrota_cuervo([1.2, 2.5, 3.7])
print(redondeados_kevin_santiago_larrota_cuervo)


