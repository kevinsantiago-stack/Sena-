

def lista_mayusculas(kevin_santiago_larrota_cuervo_lista):
    return [x.upper() for x in kevin_santiago_larrota_cuervo_lista]

print(lista_mayusculas(["hola","mundo"]))



def lista_tuplas_cuadrado(kevin_santiago_larrota_cuervo_n):
    return [(x, x**2) for x in range(1, kevin_santiago_larrota_cuervo_n+1)]

print(lista_tuplas_cuadrado(5))