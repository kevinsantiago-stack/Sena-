 

def factorial(kevin_santiago_larrota_cuervo):
    if kevin_santiago_larrota_cuervo == 0:
        return 1
    else:
        return kevin_santiago_larrota_cuervo * factorial(kevin_santiago_larrota_cuervo - 1)

print(factorial(5))



