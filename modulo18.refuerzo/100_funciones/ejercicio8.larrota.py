

def ordenar_lista(kevin_santiago_larrota_cuervo_lista):
    return sorted(kevin_santiago_larrota_cuervo_lista)

print(ordenar_lista([4,1,7,3]))




def combinar_listas(kevin_santiago_larrota_cuervo_lista1, kevin_santiago_larrota_cuervo_lista2):
    return kevin_santiago_larrota_cuervo_lista1 + kevin_santiago_larrota_cuervo_lista2

print(combinar_listas([1,2], [3,4]))