def palabras_largas(kevin_santiago_larrota_cuervo_lista, kevin_santiago_larrota_cuervo_longitud):
    return [p for p in kevin_santiago_larrota_cuervo_lista if len(p) > kevin_santiago_larrota_cuervo_longitud]

print(palabras_largas(["hola","adios","programacion"], 4))
