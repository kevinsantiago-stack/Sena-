def contar_ocurrencias(kevin_santiago_larrota_cuervo_lista, kevin_santiago_larrota_cuervo_elemento):
    return kevin_santiago_larrota_cuervo_lista.count(kevin_santiago_larrota_cuervo_elemento)

print(contar_ocurrencias([1,2,2,3,2], 2))


