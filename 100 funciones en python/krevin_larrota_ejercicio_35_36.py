def mayores_que(kevin_santiago_larrota_cuervo_lista, kevin_santiago_larrota_cuervo_valor):
    return [x for x in kevin_santiago_larrota_cuervo_lista if x > kevin_santiago_larrota_cuervo_valor]

print(mayores_que([1,5,10,2], 4))
