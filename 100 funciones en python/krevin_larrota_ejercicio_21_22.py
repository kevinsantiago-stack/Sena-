# ejemplo 21  funciones en python 

def suma_pares(kevin_santiago_larrota_cuervo_lista):
    return sum(x for x in kevin_santiago_larrota_cuervo_lista if x % 2 == 0)

print(suma_pares([1,2,3,4,5,6]))


