def en_rango(kevin_santiago_larrota_cuervo_num, kevin_santiago_larrota_cuervo_min, kevin_santiago_larrota_cuervo_max):
    return kevin_santiago_larrota_cuervo_min <= kevin_santiago_larrota_cuervo_num <= kevin_santiago_larrota_cuervo_max

print(en_rango(5, 1, 10))
